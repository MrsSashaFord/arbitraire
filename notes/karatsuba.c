#include <stdio.h>


int main(void)
{
	/*
		12345678 * 55559999
		(1234*10^4 + 5678) * (5555*10^4 + 9999)

		1234*5555*10^8 + (1234*9999 + 5678*5555)*10^4 + 5678*9999
	*/

	long a = 12345678;
	long b = 55559999;

	printf("a*b %ld\n", a*b);

	printf("%ld\n", (12340000L + 5678L) * (55550000L + 9999L));
	printf("%ld\n", (1234L*5555L*100000000) + (1234L*9999L + 5678L*5555L)*10000 + (5678L*9999L));
}
